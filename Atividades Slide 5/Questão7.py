#Leia um valor e verifique: Se é maior que 10 → “Maior que 10”; Caso contrário → “Menor ou igual a 10".

print("Digite um valor e o sistema irá verificar se ele é maior, menor ou igual a 10.")
valor = int(input("Digite aqui seu número: "))

if valor > 10:
    print("Seu número é Maior que 10!")
else:
    print("Seu número é menor ou igual a 10")
